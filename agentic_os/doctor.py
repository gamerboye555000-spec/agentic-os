"""Health checks. Exit 0 when everything passes; exit 1 with the list of
failed checks otherwise (wiring in cli.py).

Night-1 checks: DB exists · required folders exist · Obsidian Home.md
exists · every done task has evidence unless an override event exists ·
generated wikilinks point to existing generated notes · nothing outside
AOS/ was generated by sync.

Weekend hardening adds: schema_version supported · task statuses inside the
vocabulary · handoffs reference existing tasks · task-linked decisions
reference existing tasks · memory supersede pointers resolve · packs
reference existing tasks and their files exist on disk.

Complete-today hardening adds: agent registry rows well-formed · dropfile
ingest events carry their dedupe hash · generated entity notes have
well-formed frontmatter · PRAGMA integrity_check — plus one WARN-ONLY line
(code tasks done without commit evidence) that never fails the run.

U-C3 adds one more WARN-ONLY line: a bounded secret sweep over the
canonical ledger rows and event payloads, naming identifiers plus
field/pattern names only — never a stored value. Event payloads are
covered twice: the safe secret_warning/secret_fields/secret_patterns
metadata (redacted post-U-C3 events carry nothing else) and a raw scan of
legacy payload strings via the shared detector. The events.actor column
is raw-scanned too (fixed label `actor`) for legacy pre-redaction rows.
"""

from __future__ import annotations

import json
import re
import sqlite3
from dataclasses import dataclass
from pathlib import Path

from . import db, ids, obsidian, secretscan, utils
from .models import AGENT_KINDS, AGENT_NAME_RE, TASK_STATUSES

_WIKILINK = re.compile(r"\[\[([^\]|#]+)(?:[|#][^\]]*)?\]\]")

# Note stems are filesystem-derived input; \Z anchors, never $ (D-v0.2.3) —
# a newline-bearing filename must not pass the containment check.
_NOTE_PATTERNS = {
    "Tasks": re.compile(r"^T-[0-9]{4,}\Z"),
    "Runs": re.compile(r"^R-[0-9]{4,}\Z"),
    "Decisions": re.compile(r"^D-[0-9]{4,}\Z"),
    "Evidence": re.compile(r"^E-[0-9]{4,}\Z"),
    "Handoffs": re.compile(r"^H-[0-9]{4,}\Z"),
    "Memory": re.compile(r"^M-[0-9]{4,}\Z"),
    "Projects": re.compile(r"^[a-z0-9][a-z0-9._-]*\Z", re.ASCII),
    # Daily YYYY-MM-DD · weekly YYYY-Www · per-project project-<slug>.
    "Reviews": re.compile(
        r"^([0-9]{4}-[0-9]{2}-[0-9]{2}"
        r"|[0-9]{4}-W[0-9]{2}"
        r"|project-[a-z0-9][a-z0-9._-]*)\Z",
        re.ASCII,
    ),
    "Agents": re.compile(r"^[A-Za-z0-9][A-Za-z0-9._-]*\Z", re.ASCII),
}


@dataclass
class Check:
    name: str
    ok: bool
    detail: str = ""
    #: A warn-only check prints [WARN] instead of [FAIL] and never affects
    #: the exit code.
    warn_only: bool = False


#: Secret-sweep findings shown per doctor run; the rest is a count. Keeps
#: large ledgers from producing unbounded terminal output (U-C3.5).
SECRET_SWEEP_DISPLAY_LIMIT = 10

#: (entity, table, (field label, column)) — the canonical human-text
#: fields the U-C3 sweep scans; the entity name is also the render_id key.
_SWEEP_DOMAIN_FIELDS = (
    ("task", "tasks", (
        ("title", "title"),
        ("spec", "spec_md"),
        ("acceptance", "acceptance_md"),
        # No CLI write path yet, but rendered into task frontmatter and
        # pack REPO & BRANCH content — mirror/pack-bearing, so swept.
        ("assignee", "assignee"),
        ("branch_hint", "branch_hint"),
    )),
    ("run", "runs", (
        ("agent", "agent"),
        ("summary", "summary_md"),
    )),
    ("decision", "decisions", (
        ("title", "title"),
        ("decision", "decision_md"),
        ("alternatives", "alternatives_md"),
    )),
    ("evidence", "evidence", (
        ("ref", "ref"),
        ("claim", "claim"),
        # Trusted mirror/export-bearing field: the canonical row keeps a
        # warned credential-shaped provenance, so the sweep must find it.
        ("provenance", "provenance"),
    )),
    ("handoff", "handoffs", (
        ("from_agent", "from_agent"),
        ("to_agent", "to_agent"),
        ("state", "state_md"),
    )),
    ("memory", "memory", (
        ("key", "key"),
        ("value", "value_md"),
        ("source", "source"),
    )),
)


def _iter_payload_strings(value, key: str | None = None):
    """Yield (innermost key, string value) pairs from a decoded event
    payload. Strings are scanned individually so a JSON key like "key"
    or "token_estimate" can never lend keyword context to a neighboring
    value."""
    if isinstance(value, str):
        yield key or "payload", value
    elif isinstance(value, dict):
        for child_key, child in value.items():
            yield from _iter_payload_strings(child, child_key)
    elif isinstance(value, list):
        for child in value:
            yield from _iter_payload_strings(child, key)


#: A payload key doctor is willing to echo as a finding label: one of our
#: snake_case identifiers, nothing longer or stranger. Keys are stored
#: data too — a tampered event must not smuggle text out through the label.
_PAYLOAD_KEY_RE = re.compile(r"^[a-z][a-z0-9_]{0,31}\Z", re.ASCII)


def _safe_payload_key(key: str) -> str:
    if _PAYLOAD_KEY_RE.match(key) and not secretscan.scan_secrets(key):
        return key
    return "payload"


def _event_secret_metadata(payload) -> list[tuple[str, list[str]]]:
    """Read well-formed U-C3 event metadata (D-v0.2.21): the safe record a
    redacted post-U-C3 event keeps of what its payload no longer shows.
    Returns [(field, pattern names)] with every name validated against the
    fixed allowlists — secretscan.TRUSTED_FIELD_LABELS and
    secretscan.PATTERN_NAMES — so malformed or tampered metadata is
    ignored, never echoed. (Its strings still get the raw leaf scan.)"""
    if not isinstance(payload, dict) or payload.get("secret_warning") is not True:
        return []
    fields = payload.get("secret_fields")
    patterns = payload.get("secret_patterns")
    if not isinstance(fields, list) or not isinstance(patterns, list):
        return []
    safe_fields = list(
        dict.fromkeys(
            field
            for field in fields
            if isinstance(field, str)
            and field in secretscan.TRUSTED_FIELD_LABELS
        )
    )
    safe_patterns = [
        name for name in secretscan.PATTERN_NAMES if name in patterns
    ]
    if not safe_fields or not safe_patterns:
        return []
    return [(field, safe_patterns) for field in safe_fields]


def _secret_sweep_findings(conn: sqlite3.Connection) -> list[str]:
    """U-C3.5 ledger sweep: every finding names an entity type, a public
    ID (or event #id), the field, and the pattern names — never a stored
    value, excerpt, or fingerprint. Domain rows get the raw scan; events
    get the safe-metadata read plus the raw scan of legacy payloads and
    of the top-level actor column (legacy raw actors)."""
    findings: list[str] = []

    def note(label: str, fields) -> None:
        for field, hits in secretscan.scan_fields(fields):
            findings.append(f"{label} {field}: {','.join(hits)}")

    # Projects and agents are identified by ROW id: their public names
    # (slug, agent name) are themselves scanned fields, and a secret-shaped
    # one must never be echoed as the identifier of another field's hit.
    for row in conn.execute(
        "SELECT id, slug, name, repo_path, conventions_md FROM projects "
        "ORDER BY id"
    ).fetchall():
        note(
            f"project #{row['id']}",
            [
                ("slug", row["slug"]),
                ("name", row["name"]),
                ("repo_path", row["repo_path"]),
                ("conventions", row["conventions_md"]),
            ],
        )
    for entity, table, columns in _SWEEP_DOMAIN_FIELDS:
        for row in conn.execute(
            f"SELECT * FROM {table} ORDER BY id"
        ).fetchall():
            hid = ids.render_id(entity, row["id"])
            note(
                f"{entity} {hid}",
                [(field, row[column]) for field, column in columns],
            )
    for row in conn.execute(
        "SELECT id, name, invoke_hint, notes, capabilities_json FROM agents "
        "ORDER BY id"
    ).fetchall():
        capabilities = None
        if row["capabilities_json"]:
            try:
                decoded = json.loads(row["capabilities_json"])
                if isinstance(decoded, list):
                    capabilities = "\n".join(
                        item for item in decoded if isinstance(item, str)
                    )
            except ValueError:
                pass  # check 13 already reports the malformed JSON
        note(
            f"agent #{row['id']}",
            [
                ("name", row["name"]),
                ("invoke_hint", row["invoke_hint"]),
                ("notes", row["notes"]),
                ("capabilities", capabilities),
            ],
        )
    for row in conn.execute(
        "SELECT id, actor, payload_json FROM events ORDER BY id"
    ).fetchall():
        label = f"event #{row['id']}"
        # Legacy raw actor values, under the FIXED label "actor" (never
        # derived from the stored text). emit now redacts a secret-shaped
        # actor to the benign placeholder, so only pre-redaction rows can
        # hit here; redacted new events surface via their safe metadata.
        note(label, [("actor", row["actor"])])
        try:
            payload = json.loads(row["payload_json"])
        except ValueError:
            continue  # check 14 reports unparseable dropfile payloads
        # Safe metadata first (the only trace a redacted event keeps),
        # then the raw leaf scan for legacy pre-redaction payloads. Keys
        # pass _safe_payload_key so no stored text rides out as a label.
        for field, patterns in _event_secret_metadata(payload):
            findings.append(f"{label} {field}: {','.join(patterns)}")
        note(
            label,
            (
                (_safe_payload_key(field), value)
                for field, value in _iter_payload_strings(payload)
            ),
        )
    # Metadata and a legacy raw payload (or repeated payload keys) can
    # produce identical finding lines; report each distinct finding once,
    # in first-seen order — deterministic for identical ledgers.
    return list(dict.fromkeys(findings))


#: Entity folders whose notes carry frontmatter (Reviews and the top-level
#: Home/CONVENTIONS/index notes deliberately have none).
FRONTMATTER_DIRS = (
    "Projects",
    "Tasks",
    "Runs",
    "Decisions",
    "Evidence",
    "Handoffs",
    "Memory",
    "Agents",
)

_FRONTMATTER_LINE = re.compile(r"^([A-Za-z_][A-Za-z0-9_]*:( .*)?|  - .+)$")


def _frontmatter_problem(text: str) -> str | None:
    lines = text.split("\n")
    if not lines or lines[0] != "---":
        return "missing opening '---'"
    for index in range(1, len(lines)):
        if lines[index] == "---":
            return "empty frontmatter" if index == 1 else None
        if not _FRONTMATTER_LINE.match(lines[index]):
            return f"bad frontmatter line {index + 1}"
    return "missing closing '---'"


def _is_hidden(path: Path, base: Path) -> bool:
    """True when any component below `base` starts with '.' (e.g. the
    .obsidian folder Obsidian creates at the vault root)."""
    return any(
        part.startswith(".") for part in path.relative_to(base).parts
    )


def run_checks(conn: sqlite3.Connection, aos_dir: Path) -> list[Check]:
    checks: list[Check] = []
    aos_root = obsidian.vault_aos_dir(aos_dir)

    # 1. DB exists.
    db_path = aos_dir / utils.DB_FILENAME
    checks.append(Check("database exists", db_path.is_file(), str(db_path)))

    # 2. Required folders exist.
    required = [aos_dir / name for name in obsidian.WORKSPACE_DIRS]
    required.append(aos_root)
    required += [aos_root / name for name in obsidian.ENTITY_DIRS]
    missing = [str(p) for p in required if not p.is_dir()]
    checks.append(
        Check(
            "required folders exist",
            not missing,
            "; ".join(f"missing {m}" for m in missing),
        )
    )

    # 3. Obsidian Home.md exists.
    home = aos_root / "Home.md"
    checks.append(Check("Home.md exists", home.is_file(), str(home)))

    # 4. Every done task has evidence unless an override event exists.
    offenders = []
    for row in conn.execute(
        "SELECT t.id FROM tasks t WHERE t.status = 'done' "
        "AND NOT EXISTS (SELECT 1 FROM evidence e WHERE e.task_id = t.id) "
        "AND NOT EXISTS (SELECT 1 FROM events ev WHERE ev.entity = 'task' "
        "AND ev.entity_id = t.id AND ev.action = 'done_override') "
        "ORDER BY t.id"
    ).fetchall():
        offenders.append(ids.render_id("task", row["id"]))
    checks.append(
        Check(
            "done tasks have evidence (or a logged override)",
            not offenders,
            "no evidence and no override event: " + ", ".join(offenders)
            if offenders
            else "",
        )
    )

    # 5. Generated wikilinks resolve to existing generated notes.
    broken = []
    if aos_root.is_dir():
        stems = {
            path.stem
            for path in aos_root.rglob("*.md")
            if not _is_hidden(path, aos_root)
        }
        for path in sorted(aos_root.rglob("*.md")):
            if _is_hidden(path, aos_root):
                continue
            try:
                text = path.read_text(encoding="utf-8")
            except UnicodeDecodeError:
                broken.append(
                    f"{path.relative_to(aos_root).as_posix()}: not valid UTF-8"
                )
                continue
            for match in _WIKILINK.finditer(text):
                target = match.group(1).strip()
                if target not in stems:
                    broken.append(
                        f"{path.relative_to(aos_root).as_posix()} → [[{target}]]"
                    )
    checks.append(
        Check(
            "wikilinks resolve to generated notes",
            not broken,
            "; ".join(broken[:10]),
        )
    )

    # 6. Nothing outside AOS/ was generated by sync, and everything inside
    #    AOS/ is a recognized generated note. (Hidden entries such as
    #    Obsidian's own .obsidian/ folder are the user's, not sync's.)
    strays = []
    vault = aos_dir / obsidian.VAULT_DIRNAME
    if vault.is_dir():
        for path in sorted(vault.rglob("*")):
            if not path.is_file() or _is_hidden(path, vault):
                continue
            if aos_root not in path.parents:
                strays.append(path.relative_to(vault).as_posix())
                continue
            rel = path.relative_to(aos_root)
            allowed_top_level = ("Home.md", "CONVENTIONS.md") + tuple(
                f"{name}.md" for name in obsidian.INDEX_NOTES
            )
            if rel.as_posix() in allowed_top_level:
                continue
            pattern = (
                _NOTE_PATTERNS.get(rel.parts[0]) if len(rel.parts) == 2 else None
            )
            if (
                pattern is None
                or path.suffix != ".md"
                or not pattern.match(path.stem)
            ):
                strays.append(rel.as_posix())
    checks.append(
        Check(
            "mirror contains only generated AOS/ notes",
            not strays,
            "; ".join(strays[:10]),
        )
    )

    # 7. schema_version supported by this build.
    version = db.get_meta(conn, "schema_version")
    checks.append(
        Check(
            "schema_version supported",
            version == db.SCHEMA_VERSION,
            ""
            if version == db.SCHEMA_VERSION
            else f"found {version!r}, this build supports {db.SCHEMA_VERSION!r}",
        )
    )

    # 8. No task carries a status outside the vocabulary.
    marks = ",".join("?" * len(TASK_STATUSES))
    bad_status = conn.execute(
        f"SELECT id, status FROM tasks WHERE status NOT IN ({marks}) "
        "ORDER BY id",
        TASK_STATUSES,
    ).fetchall()
    checks.append(
        Check(
            "task statuses inside the vocabulary",
            not bad_status,
            "; ".join(
                f"{ids.render_id('task', row['id'])}: {row['status']!r}"
                for row in bad_status[:10]
            ),
        )
    )

    # 9. Handoffs point to existing tasks.
    orphan_handoffs = conn.execute(
        "SELECT h.id FROM handoffs h LEFT JOIN tasks t ON t.id = h.task_id "
        "WHERE t.id IS NULL ORDER BY h.id"
    ).fetchall()
    checks.append(
        Check(
            "handoffs reference existing tasks",
            not orphan_handoffs,
            "; ".join(
                ids.render_id("handoff", row["id"])
                for row in orphan_handoffs[:10]
            ),
        )
    )

    # 10. Task-linked decisions point to existing tasks.
    orphan_decisions = conn.execute(
        "SELECT d.id FROM decisions d LEFT JOIN tasks t ON t.id = d.task_id "
        "WHERE d.task_id IS NOT NULL AND t.id IS NULL ORDER BY d.id"
    ).fetchall()
    checks.append(
        Check(
            "task-linked decisions reference existing tasks",
            not orphan_decisions,
            "; ".join(
                ids.render_id("decision", row["id"])
                for row in orphan_decisions[:10]
            ),
        )
    )

    # 11. Memory supersede pointers resolve to existing memory rows.
    dangling_memory = conn.execute(
        "SELECT m.id, m.superseded_by FROM memory m "
        "LEFT JOIN memory s ON s.id = m.superseded_by "
        "WHERE m.superseded_by IS NOT NULL AND s.id IS NULL ORDER BY m.id"
    ).fetchall()
    checks.append(
        Check(
            "memory supersede pointers resolve",
            not dangling_memory,
            "; ".join(
                f"{ids.render_id('memory', row['id'])} → missing "
                f"{ids.render_id('memory', row['superseded_by'])}"
                for row in dangling_memory[:10]
            ),
        )
    )

    # 12. Packs reference existing tasks and their files exist on disk.
    pack_problems = []
    for row in conn.execute(
        "SELECT p.id, p.path, t.id AS task_ref FROM packs p "
        "LEFT JOIN tasks t ON t.id = p.task_id ORDER BY p.id"
    ).fetchall():
        pack_hid = ids.render_id("pack", row["id"])
        if row["task_ref"] is None:
            pack_problems.append(f"{pack_hid} → missing task")
        elif not (aos_dir / row["path"]).is_file():
            pack_problems.append(f"{pack_hid} → missing file {row['path']}")
    checks.append(
        Check(
            "packs reference existing tasks and files",
            not pack_problems,
            "; ".join(pack_problems[:10]),
        )
    )

    # 13. Agent registry rows are well-formed (name charset, kind
    #     vocabulary, capabilities_json a JSON array).
    bad_agents = []
    for row in conn.execute(
        "SELECT name, kind, capabilities_json FROM agents ORDER BY name"
    ).fetchall():
        problems = []
        name = row["name"] or ""
        if not AGENT_NAME_RE.match(name):
            problems.append("invalid name")
        if row["kind"] not in AGENT_KINDS:
            problems.append(f"unknown kind {row['kind']!r}")
        if row["capabilities_json"] is not None:
            try:
                value = json.loads(row["capabilities_json"])
                if not isinstance(value, list):
                    problems.append("capabilities_json is not a JSON array")
            except ValueError:
                problems.append("capabilities_json does not parse")
        if problems:
            bad_agents.append(f"{name or '(unnamed)'}: " + ", ".join(problems))
    checks.append(
        Check(
            "agent registry rows are well-formed",
            not bad_agents,
            "; ".join(bad_agents[:10]),
        )
    )

    # 14. Duplicate-dropfile protection intact: every ingest event carries
    #     the sha256 the dedupe scan relies on.
    bad_ingests = []
    for row in conn.execute(
        "SELECT id, payload_json FROM events "
        "WHERE entity = 'system' AND action = 'dropfile_ingest' ORDER BY id"
    ).fetchall():
        try:
            payload = json.loads(row["payload_json"])
        except ValueError:
            bad_ingests.append(f"event #{row['id']}: payload does not parse")
            continue
        sha = payload.get("sha256")
        if not isinstance(sha, str) or len(sha) != 64:
            bad_ingests.append(f"event #{row['id']}: missing sha256")
    checks.append(
        Check(
            "dropfile ingest events carry their dedupe hash",
            not bad_ingests,
            "; ".join(bad_ingests[:10]),
        )
    )

    # 15. Generated entity notes have well-formed frontmatter.
    bad_frontmatter = []
    for dirname in FRONTMATTER_DIRS:
        folder = aos_root / dirname
        if not folder.is_dir():
            continue
        for path in sorted(folder.glob("*.md")):
            if _is_hidden(path, aos_root):
                continue
            try:
                text = path.read_text(encoding="utf-8")
            except UnicodeDecodeError:
                continue  # check 5 already reports the encoding failure
            problem = _frontmatter_problem(text)
            if problem:
                bad_frontmatter.append(f"{dirname}/{path.name}: {problem}")
    checks.append(
        Check(
            "generated notes have well-formed frontmatter",
            not bad_frontmatter,
            "; ".join(bad_frontmatter[:10]),
        )
    )

    # 16. SQLite structural integrity.
    try:
        integrity = conn.execute("PRAGMA integrity_check").fetchone()[0]
    except sqlite3.DatabaseError as exc:
        integrity = f"error: {exc}"
    checks.append(
        Check(
            "database integrity_check passes",
            integrity == "ok",
            "" if integrity == "ok" else str(integrity)[:200],
        )
    )

    # 17 (WARN, never fatal). Code tasks closed without commit evidence —
    #     surfaced here and in the review's matching section.
    warn_offenders = [
        ids.render_id("task", row["id"])
        for row in conn.execute(
            "SELECT id FROM tasks WHERE status = 'done' AND kind = 'code' "
            "AND NOT EXISTS (SELECT 1 FROM evidence e "
            "WHERE e.task_id = tasks.id AND e.kind = 'commit') ORDER BY id"
        ).fetchall()
    ]
    checks.append(
        Check(
            "code tasks done without commit evidence",
            not warn_offenders,
            ", ".join(warn_offenders[:10]),
            warn_only=True,
        )
    )

    # 18 (WARN, never fatal). U-C3 secret sweep (D-v0.2.15): secret-shaped
    #     text sitting in otherwise valid ledger rows, or event payloads
    #     whose safe metadata (or legacy raw strings) mark one. The ledger
    #     stays an honest historical record — doctor names the affected
    #     identifiers, field and pattern names so the human can
    #     rotate/remove, and never prints a stored value. Output is bounded.
    secret_findings = _secret_sweep_findings(conn)
    shown = secret_findings[:SECRET_SWEEP_DISPLAY_LIMIT]
    hidden = len(secret_findings) - len(shown)
    detail = "; ".join(shown)
    if hidden > 0:
        detail += f" (+{hidden} more)"
    checks.append(
        Check(
            "secret-shaped text in ledger rows or event payloads",
            not secret_findings,
            detail,
            warn_only=True,
        )
    )

    return checks
