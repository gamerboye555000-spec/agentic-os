def codes_of(result) -> list[str]:
    return [finding.code for finding in result.findings]


class WorkSpecCase(unittest.TestCase):
    maxDiff = None

    def assertCodes(self, result, expected: set[str]) -> None:
        self.assertEqual(set(codes_of(result)), expected)

    def assertHasFinding(self, result, code: str, path: str | None = None):
        for finding in result.findings:
            if finding.code == code and (path is None or finding.path == path):
                return finding
        self.fail(
            f"no finding {code!r}"
            + (f" at {path!r}" if path else "")
            + f"; findings: {[(f.code, f.path) for f in result.findings]}"
        )


