class Finding:
    """One bounded, value-free diagnostic: a reason code and optional locus."""

    __slots__ = ("reason", "locus")

    def __init__(self, reason: str, locus: str | None = None) -> None:
        self.reason = reason
        self.locus = locus

    def key(self):
        return (self.reason, self.locus or "")

    def render(self) -> str:
        if self.locus is None:
            return self.reason
        return f"{self.reason} {self.locus}"


