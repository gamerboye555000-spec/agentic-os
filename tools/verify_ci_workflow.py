# Specific-code classification checks. Each is silent on the canonical file and
# fires only on a deviation that maps to a dedicated reason code.

def _check_actions(lines):
    findings = []
    for index, line in enumerate(lines):
        stripped = line.strip()
        if not stripped.startswith("uses:"):
            continue
        value = stripped[len("uses:"):].strip()
        parts = value.split()
        token = parts[0] if parts else ""
        if "@" not in token:
            continue  # local action / missing ref -> canonical recognition
        repo, _, ref = token.partition("@")
        if not _OWNER_REPO_RE.match(repo):
            continue  # reusable workflow / local path -> canonical recognition
        locus = f"line:{index + 1}"
        if repo not in ACTION_ALLOWLIST:
            findings.append(Finding("unapproved_action", locus))
        elif ref != ACTION_ALLOWLIST[repo]:
            findings.append(Finding("mutable_action_ref", locus))
    return findings


def _check_artifacts(lines):
    for line in lines:
        if "actions/upload-artifact" in line or "actions/download-artifact" in line:
            return [Finding("artifact_upload")]
    return []


def _check_downloads(lines):
    for line in lines:
        if _SHELL_DOWNLOAD_RE.search(line):
            return [Finding("shell_download")]
    return []


def _check_secrets(lines):
    for line in lines:
        if "secrets." in line:
            return [Finding("secret_reference")]
    return []


def _check_continue_on_error(lines):
    for line in lines:
        if "continue-on-error" in line:
            return [Finding("continue_on_error")]
    return []


def _check_permissions(lines):
    for line in lines:
        if _WRITE_PERMISSION_RE.match(line.strip()):
            return [Finding("write_permission")]
    return []


def _check_credentials(lines):
    findings = []
    for index, line in enumerate(lines):
        if line.strip().startswith("uses: actions/checkout@"):
            window = lines[index + 1:index + 5]
            if not any(w.strip() == "persist-credentials: false" for w in window):
                findings.append(Finding("credential_persistence", f"line:{index + 1}"))
    return findings


def _check_triggers(lines):
    findings = []
    if any("pull_request_target" in line for line in lines):
        findings.append(Finding("pull_request_target"))
    stripped = {line.strip() for line in lines}
    for trigger in FROZEN_TRIGGERS:
        if f"{trigger}:" not in stripped:
            findings.append(Finding("missing_required_trigger", trigger))
    return findings


def _check_jobs(blocks):
    findings = []
    for job_id, job_name in FROZEN_JOBS:
        body = blocks.get(job_id)
        if body is None:
            findings.append(Finding("missing_required_job", job_id))
            continue
        found = None
        for line in body:
            indent = len(line) - len(line.lstrip(" "))
            stripped = line.strip()
            if indent == 4 and stripped.startswith("name:"):
                found = stripped[len("name:"):].strip()
                break
        if found != job_name:
            findings.append(Finding("missing_required_job", job_id))
    return findings


def _check_timeouts(blocks):
    findings = []
    for job_id, _ in FROZEN_JOBS:
        body = blocks.get(job_id)
        if body is None:
            continue  # a missing job is already a missing_required_job
        if not any(line.strip().startswith("timeout-minutes:") for line in body):
            findings.append(Finding("missing_timeout", job_id))
    return findings


def _check_python_lines(lines):
    findings = []
    stripped = {line.strip() for line in lines}
    for feature_line in FROZEN_PYTHON_LINES:
        if f'python-version: "{feature_line}"' not in stripped:
            findings.append(Finding("missing_python_line"))
    return findings


def _check_full_suite(blocks):
    findings = []
    for job_id in TEST_JOB_IDS:
        body = blocks.get(job_id)
        if body is None:
            continue
        if not any(FULL_SUITE_COMMAND in line for line in body):
            findings.append(Finding("missing_full_suite", job_id))
    return findings


def _check_distribution_smoke(lines):
    joined = "\n".join(lines)
    for command in REQUIRED_SMOKE_COMMANDS:
        if command not in joined:
            return [Finding("missing_distribution_smoke", SMOKE_JOB_ID)]
    return []


# ---------------------------------------------------------------------------
# Orchestration.

def _collect_findings(path):
    raw = _read_bytes(path)
    if raw is None:
        return [Finding("missing_workflow")]
    try:
        text = raw.decode("utf-8")
    except UnicodeDecodeError:
        return [Finding("invalid_utf8")]
    if "\r" in text:
        return [Finding("crlf_present")]

    lines = text.split("\n")
    blocks = _job_blocks(lines)

    findings = []
    findings += _check_actions(lines)
    findings += _check_artifacts(lines)
    findings += _check_downloads(lines)
    findings += _check_secrets(lines)
    findings += _check_continue_on_error(lines)
    findings += _check_permissions(lines)
    findings += _check_credentials(lines)
    findings += _check_triggers(lines)
    findings += _check_jobs(blocks)
    findings += _check_timeouts(blocks)
    findings += _check_python_lines(lines)
    findings += _check_full_suite(blocks)
    findings += _check_distribution_smoke(lines)

    # Affirmative recognition backstop: anything not byte-identical to the
    # frozen canonical text that no specific code already named fails closed.
    if not findings and text != CANONICAL_TEXT:
        findings.append(Finding("unexpected_workflow_shape"))
    return findings


def _order(findings):
    ordered = []
    seen = set()
    for finding in sorted(findings, key=lambda f: f.key()):
        key = finding.key()
        if key in seen:
            continue
        seen.add(key)
        ordered.append(finding)
    return ordered


