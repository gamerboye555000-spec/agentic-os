"""Fail-closed canonical byte-grammar verifier for .github/workflows/ci.yml.

Contract: agentic-os-v0.4-u-p2-delivery-gate-contract.md §12 (D-v0.4.41).

This is deliberately NOT a general YAML parser or an Actions linter, and it does
NOT scan for forbidden substrings. It affirmatively recognizes the ONE frozen
canonical workflow: the security decision is exact equality against an embedded
canonical byte representation (`CANONICAL_TEXT`), so the file is accepted only
when every byte and construct belongs to the single frozen grammar, and any
deviation — an anchor, alias, custom tag, duplicate key, document marker,
multi-document stream, flow-style collection, quoted/escaped key, alternate
boolean spelling, job-level `permissions:` block, local action, reusable
workflow, multiline/folded `uses:`, unexpected block scalar, unknown
job/step/key/ordering, or shell indirection replacing a frozen command — fails
closed. Because the canonical text is clean, the classification checks layered
on top can never cause a false accept: they only *refine* a rejection into the
most specific reason code, defaulting to `unexpected_workflow_shape`. Even if a
classification check had a gap, the equality backstop still rejects.

Standard library only. Opens no socket, spawns no subprocess, mutates nothing,
and stays total: a hostile or corrupt file yields findings, never a traceback.

Interface:

    python3 tools/verify_ci_workflow.py
    python3 tools/verify_ci_workflow.py --workflow .github/workflows/ci.yml

Exit 0 only with zero findings; 1 with findings (one reason code per line on
stdout, deterministically ordered by (reason, locus)); 2 on a usage error.
Diagnostics are value-free and bounded: a reason code from the closed 20-code
vocabulary, optionally followed by a closed-set locus (a frozen job id, a frozen
trigger name, or `line:<n>`). Arbitrary workflow content is never echoed, so a
hostile workflow cannot use the verifier as an output channel.
"""

