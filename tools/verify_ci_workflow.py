# ---------------------------------------------------------------------------
# Reading and lightweight structure.

#: A deterministic upper bound on the workflow file, comfortably above the
#: canonical size (~13 KiB). A regular file larger than this is refused before
#: any content is decoded, so a hostile or corrupt target cannot force an
#: unbounded read or allocation.
MAX_WORKFLOW_BYTES = 1 << 20  # 1 MiB


class _ReadResult:
    """Outcome of reading the workflow target: bytes on success, else a reason.

    `reason`, when set, is an existing closed reason code — `missing_workflow`
    for an absent or unopenable target, `unexpected_workflow_shape` for a
    non-regular (directory, FIFO, device, socket, symlink) or oversized target.
    No new reason code is introduced.
    """

    __slots__ = ("data", "reason")

    def __init__(self, data: bytes | None = None, reason: str | None = None) -> None:
        self.data = data
        self.reason = reason


def _read_bytes(path) -> _ReadResult:
    """Bounded, symlink-refusing read of a single regular file.

    Opens the target with O_NOFOLLOW (never follow a final-component symlink)
    and O_NONBLOCK (never block on a FIFO with no writer) where the platform
    provides them, confirms via fstat on the descriptor that it is a regular
    file no larger than MAX_WORKFLOW_BYTES, then reads at most that bound plus
    one byte — a file that grew after fstat is refused rather than silently
    truncated into a spurious accept. The descriptor is always closed. On a
    platform without O_NOFOLLOW the open cannot refuse a final-component symlink
    at the syscall level; the byte-equality backstop still fails closed there,
    since only exactly-canonical bytes are ever accepted however they arrive.
    """
    flags = os.O_RDONLY
    flags |= getattr(os, "O_NONBLOCK", 0)
    flags |= getattr(os, "O_NOFOLLOW", 0)
    flags |= getattr(os, "O_CLOEXEC", 0)
    try:
        fd = os.open(os.fspath(path), flags)
    except FileNotFoundError:
        return _ReadResult(reason="missing_workflow")
    except OSError as exc:
        if exc.errno == errno.ELOOP:  # a symlink refused by O_NOFOLLOW
            return _ReadResult(reason="unexpected_workflow_shape")
        return _ReadResult(reason="missing_workflow")  # unopenable target
    try:
        info = os.fstat(fd)
        if not stat.S_ISREG(info.st_mode):
            return _ReadResult(reason="unexpected_workflow_shape")
        if info.st_size > MAX_WORKFLOW_BYTES:
            return _ReadResult(reason="unexpected_workflow_shape")
        chunks: list[bytes] = []
        remaining = MAX_WORKFLOW_BYTES + 1
        while remaining > 0:
            try:
                chunk = os.read(fd, min(65536, remaining))
            except BlockingIOError:  # a nonblocking pipe with no data ready
                return _ReadResult(reason="unexpected_workflow_shape")
            if not chunk:
                break
            chunks.append(chunk)
            remaining -= len(chunk)
        data = b"".join(chunks)
        if len(data) > MAX_WORKFLOW_BYTES:
            return _ReadResult(reason="unexpected_workflow_shape")
        return _ReadResult(data=data)
    finally:
        os.close(fd)


def _job_blocks(lines):
    """Map each 2-space-indented `jobs:` child id to its body lines.

    Lenient by design: correctness rests on CANONICAL_TEXT equality, so this
    only has to attribute lines well enough for the specific-code checks. An
    unexpected shape merely yields imperfect attribution, then the equality
    backstop rejects the file anyway.
    """
    blocks: dict[str, list[str]] = {}
    try:
        start = lines.index("jobs:")
    except ValueError:
        return blocks
    current = None
    body: list[str] = []
    for line in lines[start + 1:]:
        if line.strip() == "":
            if current is not None:
                body.append(line)
            continue
        indent = len(line) - len(line.lstrip(" "))
        if indent == 0:
            break
        stripped = line.strip()
        if indent == 2 and stripped.endswith(":") and not stripped.startswith("-"):
            if current is not None:
                blocks[current] = body
            current = stripped[:-1]
            body = []
        elif current is not None:
            body.append(line)
    if current is not None:
        blocks[current] = body
    return blocks


# ---------------------------------------------------------------------------
# Specific-code classification checks. Each is silent on the canonical file and
# fires only on a deviation that maps to a dedicated reason code.

def _check_actions(lines):
    findings = []
    for index, line in enumerate(lines):
        stripped = line.strip()
        if not stripped.startswith("uses:"):
            continue
        value = stripped[len("uses:"):].strip()
        parts = value.split()
        token = parts[0] if parts else ""
        if "@" not in token:
            continue  # local action / missing ref -> canonical recognition
        repo, _, ref = token.partition("@")
        if not _OWNER_REPO_RE.match(repo):
            continue  # reusable workflow / local path -> canonical recognition
        locus = f"line:{index + 1}"
        if repo not in ACTION_ALLOWLIST:
            findings.append(Finding("unapproved_action", locus))
        elif ref != ACTION_ALLOWLIST[repo]:
            findings.append(Finding("mutable_action_ref", locus))
    return findings


def _check_artifacts(lines):
    for line in lines:
        if "actions/upload-artifact" in line or "actions/download-artifact" in line:
            return [Finding("artifact_upload")]
    return []


def _check_downloads(lines):
    for line in lines:
        if _SHELL_DOWNLOAD_RE.search(line):
            return [Finding("shell_download")]
    return []


def _check_secrets(lines):
    for line in lines:
        if "secrets." in line:
            return [Finding("secret_reference")]
    return []


def _check_continue_on_error(lines):
    for line in lines:
        if "continue-on-error" in line:
            return [Finding("continue_on_error")]
    return []


def _check_permissions(lines):
    for line in lines:
        if _WRITE_PERMISSION_RE.match(line.strip()):
            return [Finding("write_permission")]
    return []


def _check_credentials(lines):
    findings = []
    for index, line in enumerate(lines):
        if line.strip().startswith("uses: actions/checkout@"):
            window = lines[index + 1:index + 5]
            if not any(w.strip() == "persist-credentials: false" for w in window):
                findings.append(Finding("credential_persistence", f"line:{index + 1}"))
    return findings


def _check_triggers(lines):
    findings = []
    if any("pull_request_target" in line for line in lines):
        findings.append(Finding("pull_request_target"))
    stripped = {line.strip() for line in lines}
    for trigger in FROZEN_TRIGGERS:
        if f"{trigger}:" not in stripped:
            findings.append(Finding("missing_required_trigger", trigger))
    return findings


def _check_jobs(blocks):
    findings = []
    for job_id, job_name in FROZEN_JOBS:
        body = blocks.get(job_id)
        if body is None:
            findings.append(Finding("missing_required_job", job_id))
            continue
        found = None
        for line in body:
            indent = len(line) - len(line.lstrip(" "))
            stripped = line.strip()
            if indent == 4 and stripped.startswith("name:"):
                found = stripped[len("name:"):].strip()
                break
        if found != job_name:
            findings.append(Finding("missing_required_job", job_id))
    return findings


def _check_timeouts(blocks):
    findings = []
    for job_id, _ in FROZEN_JOBS:
        body = blocks.get(job_id)
        if body is None:
            continue  # a missing job is already a missing_required_job
        if not any(line.strip().startswith("timeout-minutes:") for line in body):
            findings.append(Finding("missing_timeout", job_id))
    return findings


def _check_python_lines(lines):
    findings = []
    stripped = {line.strip() for line in lines}
    for feature_line in FROZEN_PYTHON_LINES:
        if f'python-version: "{feature_line}"' not in stripped:
            findings.append(Finding("missing_python_line"))
    return findings


def _check_full_suite(blocks):
    findings = []
    for job_id in TEST_JOB_IDS:
        body = blocks.get(job_id)
        if body is None:
            continue
        if not any(FULL_SUITE_COMMAND in line for line in body):
            findings.append(Finding("missing_full_suite", job_id))
    return findings


def _check_distribution_smoke(lines):
    joined = "\n".join(lines)
    for command in REQUIRED_SMOKE_COMMANDS:
        if command not in joined:
            return [Finding("missing_distribution_smoke", SMOKE_JOB_ID)]
    return []


# ---------------------------------------------------------------------------
# Orchestration.

def _collect_findings(path):
    result = _read_bytes(path)
    if result.data is None:
        return [Finding(result.reason or "missing_workflow")]
    raw = result.data
    try:
        text = raw.decode("utf-8")
    except UnicodeDecodeError:
        return [Finding("invalid_utf8")]
    if "\r" in text:
        return [Finding("crlf_present")]

    lines = text.split("\n")
    blocks = _job_blocks(lines)

    findings = []
    findings += _check_actions(lines)
    findings += _check_artifacts(lines)
    findings += _check_downloads(lines)
    findings += _check_secrets(lines)
    findings += _check_continue_on_error(lines)
    findings += _check_permissions(lines)
    findings += _check_credentials(lines)
    findings += _check_triggers(lines)
    findings += _check_jobs(blocks)
    findings += _check_timeouts(blocks)
    findings += _check_python_lines(lines)
    findings += _check_full_suite(blocks)
    findings += _check_distribution_smoke(lines)

    # Affirmative recognition backstop: anything not byte-identical to the
    # frozen canonical text that no specific code already named fails closed.
    if not findings and text != CANONICAL_TEXT:
        findings.append(Finding("unexpected_workflow_shape"))
    return findings


def _order(findings):
    ordered = []
    seen = set()
    for finding in sorted(findings, key=lambda f: f.key()):
        key = finding.key()
        if key in seen:
            continue
        seen.add(key)
        ordered.append(finding)
    return ordered


def _parse_args(argv):
    workflow = None
    index = 0
    count = len(argv)
    while index < count:
        arg = argv[index]
        if arg == "--workflow":
            if index + 1 >= count:
                raise _UsageError("--workflow requires a PATH")
            workflow = argv[index + 1]
            index += 2
            continue
        if arg.startswith("--workflow="):
            workflow = arg[len("--workflow="):]
            index += 1
            continue
        raise _UsageError(f"unexpected argument: {arg}")
    return workflow


def _default_path():
    return Path(__file__).resolve().parent.parent / DEFAULT_WORKFLOW


def main(argv=None) -> int:
    if argv is None:
        argv = sys.argv[1:]
    try:
        workflow = _parse_args(list(argv))
    except _UsageError:
        sys.stderr.write(_USAGE + "\n")
        return 2

    path = Path(workflow) if workflow is not None else _default_path()

    # Total top-level guard: any unexpected internal failure becomes exactly one
    # value-free `internal_error` finding and exit 1 — never a traceback, never
    # an output channel for the exception message or file content.
    try:
        findings = _collect_findings(path)
    except Exception:
        findings = [Finding("internal_error")]

    findings = _order(findings)
    if findings:
        sys.stdout.write("".join(f.render() + "\n" for f in findings))
    return 1 if findings else 0


if __name__ == "__main__":
    sys.exit(main())
