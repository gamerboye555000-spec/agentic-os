class Finding:
    """One bounded, value-free diagnostic: a reason code and optional locus."""

    __slots__ = ("reason", "locus")

    def __init__(self, reason: str, locus: str | None = None) -> None:
        self.reason = reason
        self.locus = locus

    def key(self):
        return (self.reason, self.locus or "")

    def render(self) -> str:
        if self.locus is None:
            return self.reason
        return f"{self.reason} {self.locus}"


class _UsageError(Exception):
    """A command-line usage error (exit 2), distinct from a workflow finding."""


# ---------------------------------------------------------------------------
# Reading and lightweight structure.

def _read_bytes(path) -> bytes | None:
    try:
        return Path(path).read_bytes()
    except OSError:
        return None


def _job_blocks(lines):
    """Map each 2-space-indented `jobs:` child id to its body lines.

    Lenient by design: correctness rests on CANONICAL_TEXT equality, so this
    only has to attribute lines well enough for the specific-code checks. An
    unexpected shape merely yields imperfect attribution, then the equality
    backstop rejects the file anyway.
    """
    blocks: dict[str, list[str]] = {}
    try:
        start = lines.index("jobs:")
    except ValueError:
        return blocks
    current = None
    body: list[str] = []
    for line in lines[start + 1:]:
        if line.strip() == "":
            if current is not None:
                body.append(line)
            continue
        indent = len(line) - len(line.lstrip(" "))
        if indent == 0:
            break
        stripped = line.strip()
        if indent == 2 and stripped.endswith(":") and not stripped.startswith("-"):
            if current is not None:
                blocks[current] = body
            current = stripped[:-1]
            body = []
        elif current is not None:
            body.append(line)
    if current is not None:
        blocks[current] = body
    return blocks


